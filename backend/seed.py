async def seed():
    async with AsyncSessionLocal() as db:
        # Проверяем, есть ли уже тестовые данные
        existing = (await db.execute(
            select(User).where(User.username == "chef_marina")
        )).scalar_one_or_none()

        if existing:
            print("[seed] Тестовые данные уже существуют, пропускаем.")
            return

        print("[seed] Создаём тестовых пользователей...")
        user_map: dict[str, User] = {}
        for u in USERS:
            user = User(
                username=u["username"],
                email=u["email"],
                password_hash=hash_password(u["password"]),
                bio=u.get("bio"),
            )
            db.add(user)
            await db.flush()
            user_map[u["username"]] = user

        print("[seed] Создаём тестовые рецепты...")
        tag_cache: dict[str, Tag] = {}

        for r in RECIPES:
            author = user_map[r["author"]]
            recipe = Recipe(
                title=r["title"],
                description=r["description"],
                cook_time=r["cook_time"],
                servings=r["servings"],
                cuisine=r["cuisine"],
                category=r["category"],
                difficulty=r["difficulty"],
                author_id=author.id,
            )
            db.add(recipe)
            await db.flush()

            # Ингредиенты
            for i, (name, amount, unit) in enumerate(r["ingredients"]):
                db.add(Ingredient(
                    recipe_id=recipe.id,
                    name=name,
                    amount=amount if amount else None,
                    unit=unit if unit else None,
                ))

            # Шаги
            for i, desc in enumerate(r["steps"]):
                db.add(Step(recipe_id=recipe.id, order=i + 1, description=desc))

            # Теги — вставляем напрямую в ассоциативную таблицу (lazy load не работает в async)
            for tag_name in r["tags"]:
                if tag_name not in tag_cache:
                    existing_tag = (await db.execute(
                        select(Tag).where(Tag.name == tag_name)
                    )).scalar_one_or_none()
                    if existing_tag:
                        tag_cache[tag_name] = existing_tag
                    else:
                        t = Tag(name=tag_name)
                        db.add(t)
                        await db.flush()
                        tag_cache[tag_name] = t
                await db.execute(
                    recipe_tags.insert().values(recipe_id=recipe.id, tag_id=tag_cache[tag_name].id)
                )

        await db.commit()
        print(f"[seed] Готово! Создано {len(USERS)} пользователей и {len(RECIPES)} рецептов.")
        print("[seed] Тестовые аккаунты:")
        for u in USERS:
            print(f"  {u['username']} / {u['password']}")


if __name__ == "__main__":
    asyncio.run(seed())
